#!/bin/bash

# Тестируем множественные одновременные подключения
echo "Тестируем множественные подключения..."

# Запускаем 5 клиентов одновременно
for i in {1..5}; do
    java -jar build/libs/Task_1J.jar client localhost 8080 "user$i" &
done

# Ждем завершения всех клиентов
wait

echo "Все клиенты завершили работу"
